#Faça um programa que receba dois números, calcule e mostre a divisão do primeiro número pelo segundo.
#Sabe-se que o segundo número não pode ser zero, portanto não é necessário se preocupar com validações.

#Receba o 1º número:
numero1 = float(input("Entre com o 1º número:"))
#Receba o 2º número:
numero2 = float(input("Entre com o 2º número:"))
#Receba o resultado da divisão 1º número pelo 2º número:
resultadoDivisao = numero1/numero2
#Imprima o resultado da divisão:
print("O resultado da divisão é",resultadoDivisao)

