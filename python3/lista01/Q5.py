#Faça um programa que receba o preço de um produto, calcule e mostre o novo preço,
#Sabendo-se que este sofreu um desconto de 10%

valorDesconto = 0.9
precoProduto = float(input("Entre com o preço do produto:"))
precoProduto = precoProduto*valorDesconto
print("O valor do produto com desconto de 10% é:",precoProduto)
