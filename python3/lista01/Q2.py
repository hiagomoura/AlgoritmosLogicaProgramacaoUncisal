#Faça um programa que receba três números, calcule e mostre a multiplicação desses três números.

#Receba o primeiro número:
print("Entre com o 1ª número: ")
numero1 = float(input())
#Receba o segundo número:
numero2 = float(input("Entre com o 2ª número: "))
#Receba o terceiro número:
numero3 = float(input("Entre com o 3ª número: "))
#Receba o resultado da multiplicação dos 3 números:
resultadoMultiplicacao = numero1*numero2*numero3
#Imprima o resultado da multiplicação dos 3 números:
print("O resultado da multiplicação é:",resultadoMultiplicacao)