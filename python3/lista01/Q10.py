#Faça um programa que calcule e mostre a área de um quadrado.
#Sabe-se que: A = lado * lado

#Receba o valor do lado do quadrado:
ladoQuadrado = float(input("Entre com a medida do lado do quadrado: "))
#Receba o valor do cálculo da área do quadrado:
areaQuadrado = ladoQuadrado**2
#Imprima o valor da área do quadrado:
print("A área desse quadrado tem o valor de:",areaQuadrado)
