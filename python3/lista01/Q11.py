#Faça um programa que calcule e mostre a área de um losango.
#Sabe-se que: A = (diagonal maior * diagonal menor)/2

#Receba o valor da diagonal maior do losango:
diagonalMaior = float(input("Entre com o valor da diagonal maior do losango: "))
#Receba o valor da diagonal menor do losango:
diagonalMenor = float(input("Entre com o valor da diagonal menor do losango: "))
#Receba o valor do cálculo da área do losango:
areaLosango = (diagonalMaior * diagonalMenor)/2
#Imprima o valor da área do losango:
print("A área desse losango é: ",areaLosango)




