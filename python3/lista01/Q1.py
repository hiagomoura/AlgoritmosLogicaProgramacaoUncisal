#Faça um programa que receba dois números, calcule e mostre a subtração do primeiro número pelo segundo.

#Receba o primeiro número:
print("Entre com o 1º Número: ")
numero1 = float(input())
#Receba o segundo número:
print("Entre com o 2º Número: ")
numero2 = float(input())
#Receba o resultado da subtração do 1º número menos o 2º número:
resultadoSubtracao = numero1-numero2
#Imprima o resultado da subtração na tela
print("Resultado da subtração é igual a:",resultadoSubtracao)
